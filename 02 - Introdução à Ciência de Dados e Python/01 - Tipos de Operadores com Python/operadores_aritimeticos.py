# Operadores Aritimeticos
produto_1 = 10
produto_2 = 20


print('ADICAO é:',produto_1 +  produto_2) # ADICAO
print('SUBTRACAO é:',produto_1 -  produto_2) # SUBTRACAO
print('DIVISAO é:',produto_1 /  produto_2) # DIVISAO
print('DIVISAO_INTEIRA é:',produto_1 // produto_2) # DIVISAO_INTEIRA
print('MULTIPLICACAO é:',produto_1 *  produto_2) # MULTIPLICACAO
print('PORCENTAGEM é:',produto_1 %  produto_2) # PORCENTAGEM
print('EXPONENCIACAO é:',produto_1 ** produto_2) # EXPONENCIACAO


# Operacao
x = (10 + 5) * 4
print('Resultado é:', x)

y = 10 / 2 + 25 * 2 ** 2
print('Resultado é:', y)

# Operacao com boa pratica em python
y = (10 / 2) + ((25 * 2) ** 2)
print('Resultado é:', y)
